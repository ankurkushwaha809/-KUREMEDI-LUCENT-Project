const fs = require("fs");
const path = require("path");

const rootDir = path.resolve(__dirname, "..");
const themePath = path.join(rootDir, "shared", "ui-theme.json");
const websiteThemePath = path.join(rootDir, "website", "src", "app", "theme.generated.css");
const appThemePath = path.join(rootDir, "app", "constants", "theme.generated.ts");
const watchMode = process.argv.includes("--watch");

function readTheme() {
  const raw = fs.readFileSync(themePath, "utf8");
  return JSON.parse(raw);
}

function ensureDir(filePath) {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
}

function writeWebsiteTheme(theme) {
  const c = theme.colors || {};
  const content = `/* Auto-generated by Frontend/scripts/sync-ui-theme.js. Do not edit directly. */
:root {
  --theme-brand: "${theme.brand || "Lucent"}";
  --theme-background: ${c.background || "#ffffff"};
  --theme-foreground: ${c.foreground || "#171717"};
  --theme-primary: ${c.primary || "#0f766e"};
  --theme-primary-strong: ${c.primaryStrong || "#0d9488"};
  --theme-accent: ${c.accent || "#14b8a6"};
  --theme-card: ${c.card || "#ffffff"};
  --theme-muted: ${c.muted || "#6b7280"};
  --theme-border: ${c.border || "#e5e7eb"};
  --theme-success: ${c.success || "#16a34a"};
  --theme-warning: ${c.warning || "#d97706"};
  --theme-danger: ${c.danger || "#dc2626"};

  /* Keep existing teal-* Tailwind classes in sync with shared brand color. */
  --color-teal-50: color-mix(in srgb, ${c.primary || "#0f766e"} 8%, white);
  --color-teal-100: color-mix(in srgb, ${c.primary || "#0f766e"} 16%, white);
  --color-teal-200: color-mix(in srgb, ${c.primary || "#0f766e"} 28%, white);
  --color-teal-300: color-mix(in srgb, ${c.primary || "#0f766e"} 40%, white);
  --color-teal-400: color-mix(in srgb, ${c.primary || "#0f766e"} 55%, white);
  --color-teal-500: color-mix(in srgb, ${c.primary || "#0f766e"} 70%, white);
  --color-teal-600: color-mix(in srgb, ${c.primary || "#0f766e"} 85%, white);
  --color-teal-700: ${c.primary || "#0f766e"};
  --color-teal-800: color-mix(in srgb, ${c.primary || "#0f766e"} 84%, black);
  --color-teal-900: color-mix(in srgb, ${c.primary || "#0f766e"} 66%, black);
}
`;

  ensureDir(websiteThemePath);
  fs.writeFileSync(websiteThemePath, content, "utf8");
}

function writeAppTheme(theme) {
  const c = theme.colors || {};
  const content = `// Auto-generated by Frontend/scripts/sync-ui-theme.js. Do not edit directly.
export const SharedTheme = {
  brand: ${JSON.stringify(theme.brand || "Lucent")},
  colors: {
    background: ${JSON.stringify(c.background || "#ffffff")},
    foreground: ${JSON.stringify(c.foreground || "#171717")},
    primary: ${JSON.stringify(c.primary || "#0f766e")},
    primaryStrong: ${JSON.stringify(c.primaryStrong || "#0d9488")},
    accent: ${JSON.stringify(c.accent || "#14b8a6")},
    card: ${JSON.stringify(c.card || "#ffffff")},
    muted: ${JSON.stringify(c.muted || "#6b7280")},
    border: ${JSON.stringify(c.border || "#e5e7eb")},
    success: ${JSON.stringify(c.success || "#16a34a")},
    warning: ${JSON.stringify(c.warning || "#d97706")},
    danger: ${JSON.stringify(c.danger || "#dc2626")},
  },
} as const;
`;

  ensureDir(appThemePath);
  fs.writeFileSync(appThemePath, content, "utf8");
}

function walkFiles(dir, files = []) {
  if (!fs.existsSync(dir)) {
    return files;
  }

  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      walkFiles(fullPath, files);
      continue;
    }
    files.push(fullPath);
  }
  return files;
}

function syncAppInlineBrandColors(theme) {
  const c = theme.colors || {};
  const appRoots = [
    path.join(rootDir, "app", "app"),
    path.join(rootDir, "app", "components"),
    path.join(rootDir, "app", "src"),
  ];

  const allowedExtensions = new Set([".ts", ".tsx", ".js", ".jsx"]);
  const replacements = [
    {
      pattern: /#0d9488/gi,
      value: c.primaryStrong || "#0d9488",
    },
    {
      pattern: /#0f766e/gi,
      value: c.primary || "#0f766e",
    },
    {
      pattern: /#14b8a6/gi,
      value: c.accent || "#14b8a6",
    },
  ];

  let updatedFiles = 0;

  for (const dir of appRoots) {
    for (const file of walkFiles(dir)) {
      if (!allowedExtensions.has(path.extname(file))) {
        continue;
      }

      const original = fs.readFileSync(file, "utf8");
      let next = original;

      for (const replacement of replacements) {
        next = next.replace(replacement.pattern, replacement.value);
      }

      if (next !== original) {
        fs.writeFileSync(file, next, "utf8");
        updatedFiles += 1;
      }
    }
  }

  if (updatedFiles > 0) {
    console.log(`Updated inline app brand colors in ${updatedFiles} files.`);
  }
}

function main() {
  const theme = readTheme();
  writeWebsiteTheme(theme);
  writeAppTheme(theme);
  syncAppInlineBrandColors(theme);
  console.log("UI theme synchronized for website and app.");
}

if (watchMode) {
  main();
  console.log("Watching Frontend/shared/ui-theme.json for changes...");
  fs.watch(themePath, { persistent: true }, () => {
    try {
      main();
    } catch (error) {
      console.error("Failed to synchronize UI theme:", error.message);
    }
  });
} else {
  main();
}